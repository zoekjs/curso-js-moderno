// Incrementos y decrementos

let puntaje = 5;

//incrementos y decrementos en 1
puntaje++; //5 el ++ al final, primero muestra y luego suma 1
++puntaje; //6 el ++ al inicio, primero suma y luego muestra

puntaje--; //5
--puntaje; //4

//incrementos y decrementos en cantidades mayores
puntaje += 3; //8
puntaje -= 2;




puntaje++;


console.log(puntaje);