// Creación de números

/* en JavaScript al no especificar el tipo de dato de las variables, todos los 
números se crean de la misma manera */

const numero1 = 30;
const numero2 = 20;
const numero3 = 20.20;
const numero4 = .102030;
const numero5 = -10;

console.log(numero1);
console.log(numero2);
console.log(numero3);
console.log(numero4);
console.log(numero5);


