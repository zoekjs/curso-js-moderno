// Crear Funciones en JS - Function expression y Function Declaration

// Son una serie de instrucciones o procedimientos que realizan una acción
// permiten tener un código más ordenado y son reutilizables

//Declaración de funcion:
function sumar() {
    console.log(2 + 2);
}

sumar();

// Expresión de función (Function Expression)
const sumar2 = function() {
    console.log(3 + 3);
}

sumar2();