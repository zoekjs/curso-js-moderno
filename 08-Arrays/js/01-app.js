// Crear Arrays en JavaScript
// Un arreglo es una variable que puede contener multiples elementos, un ejemplo sería un carrito de compras

// Los arreglos funcionan con indices para indicar la posición de cada elemento
// siendo la primera posición el indice 0
const numeros = [10, 20, 30];

// Otra forma de crear arreglos
const meses = new Array('enero', 'febrero', 'marzo');

console.log(numeros);
console.log(meses);


// Un arreglo que contiene datos de todo tipo
const deTodo = ["Hola", 10, true, "si", null, {nombre: 'felipe', meta: 'programador'}];
console.log(deTodo);
